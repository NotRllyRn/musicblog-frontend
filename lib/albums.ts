export function getAlbumBySlug(slug: string) {
  return albums.find((album) => album.slug === slug)
}

export async function fetchAlbumBySlug(slug: string) {
  return getAlbumBySlug(slug) ?? null
}

export function searchAlbums(query: string, source = albums) {
  const normalized = query.trim().toLowerCase()

  if (!normalized) {
    return source
  }

  return source.filter((album) => {
    const searchable = [
      album.title,
      album.artist,
      album.excerpt,
      album.content,
      album.listenDate ?? "",
      ...(album.tags ?? []),
    ]
      .join(" ")
      .toLowerCase()

    return searchable.includes(normalized)
  })
}
